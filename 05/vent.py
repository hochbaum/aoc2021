class Point:
    """
    Point represents a single coordinate on the field: (X, Y).
    """
    def __init__(self, x, y):
        self.x = x
        self.y = y

    # Overriding hashing and equality for the dictionary.
    def __hash__(self):
        return hash((self.x, self.y))  

    def __eq__(self, other):
        return (self.x, self.y) == (other.x, other.y)      

class Line:
    """
    Line represents a line of vents, reaching from Point start to Point end.
    """
    def __init__(self, start, end):
        self.start = start
        self.end = end
        self.diagonal = (self.start.x != self.end.x) and (self.start.y != self.end.y) 

    def __str__(self):
        return f"{self.start} -> {self.end}"   

class Field:
    """
    Field represents the ocean floor of the challenge. It allows for addition of vent Lines
    and counts overlaps.
    """
    def __init__(self, do_diagonal):
        self.points = {}
        self.do_diagonal = do_diagonal

    def add_line(self, line):
        """
        Adds a new Line of vents to the field and counts the overlaps.
        """
        start = line.start
        end = line.end

        dX = -1 if start.x > end.x else 1
        dY = -1 if start.y > end.y else 1

        if not line.diagonal:
            for x in range(start.x, end.x + dX, dX):
                for y in range(start.y, end.y + dY, dY):
                    point = Point(x, y)
                    self.points[point] = self.points.get(point, 0) + 1

        # Only do this if diagonal mode is active.            
        elif self.do_diagonal:
            x = start.x
            y = start.y
            while ((x - end.x - dX) and (y - end.y - dY)):
                point = Point(x, y)
                self.points[point] = self.points.get(point, 0) + 1
                x += dX
                y += dY

    def find_overlaps(self):
        """
        Returns how often overlaps happened more than twice.
        """
        n = 0
        for amount in self.points.values():
            if amount >= 2: n += 1
        return n